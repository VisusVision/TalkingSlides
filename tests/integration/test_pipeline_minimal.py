class TestAudioDuration:
    def test_silent_wav_duration(self, workspace, sample_audio):
        dur = get_audio_duration(str(sample_audio))
        assert 2.0 <= dur <= 3.0, f"Unexpected duration: {dur}"


class TestCreateSlideVideo:
    def test_creates_mp4(self, workspace, sample_image, sample_audio):
        out = workspace / "parts" / "part_01.mp4"
        result = create_slide_video(
            str(sample_image),
            str(sample_audio),
            str(out),
            resolution=(640, 360),
        )
        assert Path(result).exists()
        assert Path(result).stat().st_size > 0

    def test_duration_close_to_audio(self, workspace, sample_image, sample_audio):
        out = workspace / "parts" / "part_01b.mp4"
        create_slide_video(str(sample_image), str(sample_audio), str(out), resolution=(640, 360))
        dur = get_audio_duration(str(out))
        # Should be within 0.5 s of the audio
        assert abs(dur - 2.5) < 0.5, f"Video duration {dur}s far from audio 2.5s"

    def test_duration_can_include_short_slide_hold(self, workspace, sample_image, sample_audio):
        out = workspace / "parts" / "part_01c.mp4"
        create_slide_video(
            str(sample_image),
            str(sample_audio),
            str(out),
            resolution=(640, 360),
            duration_sec=2.8,
        )
        dur = get_audio_duration(str(out))
        assert abs(dur - 2.8) < 0.35, f"Video duration {dur}s far from requested 2.8s"


class TestAudioSyncCleanup:
    def test_trim_trailing_silence_reduces_dead_air(self, workspace):
        audio = _make_tone_then_silence(workspace / "tone_then_silence.wav", tone_sec=1.0, silence_sec=2.0)
        before = get_audio_duration(str(audio))
        trim_trailing_silence(str(audio))
        after = get_audio_duration(str(audio))

        assert before > 2.5
        assert after < before - 1.0
        assert 0.5 <= after <= 1.6


class TestConcatVideos:
    def test_concat_two_clips(self, workspace, sample_image, sample_audio):
        parts_dir = workspace / "concat_parts"

        # Create two small clips
        clips = []
        for i in range(1, 3):
            audio = _make_silent_wav(parts_dir / f"a{i}.wav", duration_sec=1.0)
            img   = _make_blank_png(parts_dir / f"s{i}.png", 640, 360)
            clip  = str(parts_dir / f"clip_{i}.mp4")
            create_slide_video(str(img), str(audio), clip, resolution=(640, 360))
            clips.append(clip)

        out = str(workspace / "final" / "lesson.mp4")
        result = concat_videos(clips, out)

        assert Path(result).exists()
        # Combined duration should be ≈ 2 s
        dur = get_audio_duration(str(result))
        assert 1.5 <= dur <= 3.0, f"Concatenated duration {dur}s out of expected range"


class TestGenerateSrt:
    def test_srt_file_created(self, workspace):
        srt_path = str(workspace / "lesson.srt")
        result = generate_srt(
            ["Hello world", "This is slide two"],
            [3.0, 4.5],
            srt_path,
        )
        content = Path(result).read_text(encoding="utf-8")
        assert "00:00:00,000 --> 00:00:03,000" in content
        assert "00:00:03,000 --> 00:00:07,500" in content
        assert "Hello world" in content
        assert "This is slide two" in content

    def test_mismatched_lengths_raise(self, workspace):
        with pytest.raises(ValueError, match="equal length"):
            generate_srt(["A"], [1.0, 2.0], str(workspace / "bad.srt"))


