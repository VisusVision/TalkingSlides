def _check_ffmpeg() -> bool:
    try:
        subprocess.run(["ffmpeg", "-version"], capture_output=True, check=True)
        return True
    except (FileNotFoundError, subprocess.CalledProcessError):
        return False


def _make_silent_wav(path: Path, duration_sec: float = 2.0) -> Path:
    """Generate a silent WAV file using ffmpeg (no network needed)."""
    path.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi",
            "-i", f"anullsrc=r=44100:cl=mono",
            "-t", str(duration_sec),
            "-c:a", "pcm_s16le",
            str(path),
        ],
        capture_output=True,
        check=True,
    )
    return path


def _make_tone_then_silence(path: Path, tone_sec: float = 1.0, silence_sec: float = 2.0) -> Path:
    path.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi", "-i", f"sine=frequency=880:duration={tone_sec}",
            "-f", "lavfi", "-i", f"anullsrc=r=44100:cl=mono:d={silence_sec}",
            "-filter_complex", "[0:a][1:a]concat=n=2:v=0:a=1[outa]",
            "-map", "[outa]",
            str(path),
        ],
        capture_output=True,
        check=True,
    )
    return path


def _make_blank_png(path: Path, w: int = 640, h: int = 360) -> Path:
    """Generate a blank black PNG using ffmpeg (no PIL needed)."""
    path.parent.mkdir(parents=True, exist_ok=True)
    subprocess.run(
        [
            "ffmpeg", "-y",
            "-f", "lavfi",
            "-i", f"color=c=black:s={w}x{h}:r=1",
            "-frames:v", "1",
            str(path),
        ],
        capture_output=True,
        check=True,
    )
    return path


